function redirect(a)
{
    if (a == "education")
    {
        window.open("education.html","_blank");
    }
    else if (a == "resume")
    {
        window.open("resume.html","_blank");
    }
    else if (a == "contact")
    {
        window.open("contact.html","_blank"); 
    }
    else if (a == "home")
    {
        window.open("index.html", "_self");
    }
}